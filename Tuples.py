# Tuples are immutable elements
# Comma separated elements within parenthesis
Tuple1 = ("Simon", 1, 2.0,("i",3,2.5))

#Tuples are accessible via index
Tuple1[0] # Output 1

#Tuples concatenation 
Tuple2 = Tuple1 + (1,"Kiragu")

#Slicing tuples
#The last index is one larger than the index we want to access

Tuple2[0:3] # Outputs "Simon", 1 ,2.0